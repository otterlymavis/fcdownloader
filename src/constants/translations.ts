/**
 * Normalizes dynamic parameter format and returns translated text.
 */
export function translate(
  key: TranslationKey,
  language: CommonLanguageCode,
  params?: Record<string, string | number>
): string {
  const dict = TRANSLATIONS[language] || TRANSLATIONS.en;
  let text = dict[key] || TRANSLATIONS.en[key] || '';
  if (params) {
    Object.entries(params).forEach(([k, v]) => {
      text = text.replace(new RegExp(`{${k}}`, 'g'), String(v));
    });
  }
  return text;
}

/**
 * Returns the best system language matching CommonLanguageCode or falls back to 'en'.
 */
export function getSystemLanguage(): CommonLanguageCode {
  try {
    const tags = runtimeLanguageTags();
    for (const tag of tags) {
      const code = normalizeLanguageTag(tag);
      if (code && TRANSLATIONS[code]) {
        return code;
      }
    }
  } catch {}
  return 'en';
}
